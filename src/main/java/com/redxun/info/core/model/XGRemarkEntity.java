package com.redxun.info.core.model;

public class XGRemarkEntity {
	
	private String title;
	private int score;
	private String modelNum;
	private String highCommentPercent;
	private String imageUrl;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public String getModelNum() {
		return modelNum;
	}
	public void setModelNum(String modelNum) {
		this.modelNum = modelNum;
	}
	public String getHighCommentPercent() {
		return highCommentPercent;
	}
	public void setHighCommentPercent(String highCommentPercent) {
		this.highCommentPercent = highCommentPercent;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

}
